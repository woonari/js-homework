const tween = gsap.to(".control-wrap .car", {
  duration: 4,
  x: 300,
  ease: "none",
  paused: true,
});

const buttons = document.querySelectorAll(".control-wrap button");

buttons.forEach((button, index) => {
  button.addEventListener("click", () => {
    switch (index) {
      case 0:
        tween.play(); // 정방향 재생
        break;
      case 1:
        tween.pause(); // 일시정지
        break;
      case 2:
        tween.resume(); // 일시정지에서 시작
        break;
      case 3:
        tween.reverse(); // 역재생
        break;
      case 4:
        tween.restart(); // 처음부터 다시재생
        break;
    }
  });
});
